print ('Какое бы число Вы ни ввели, ряд арифметических операций превратит в конце это число в 4')
a = input('Введите любое число: ')
x = int(a)
y = int(x*2)
z = int(y+8)
w = int(z/2)
q = int(w-x)
print('Число', x, 'умноженное', 'на', 2, 'равно', y)
print('Число', y, 'увеличенное', 'на', 8, 'равно', z)
print('Число', z, 'разделенное', 'на', 2, 'равно', w)
print('Число', w, 'уменьшенное', 'на', x, 'равно', q)
print('Финальный', 'результат', '-', q)
